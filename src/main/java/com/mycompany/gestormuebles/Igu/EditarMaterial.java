    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        // TODO add your handling code here:
        String nombreM = TxtNombreMaterial.getText();
        String cantidadM = TxtCantidadMaterial.getText();
        String proveedorM = (String) CbxProveedorMaterial.getSelectedItem();
        String categoriaM = (String) CbxCategoriaMaterial.getSelectedItem();

        if (nombreM.isEmpty() || cantidadM.isEmpty() || proveedorM.isEmpty() || categoriaM.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por Favor los campos deben estar completados", "Información", JOptionPane.INFORMATION_MESSAGE);

        } else {
            int cantidadMf = Integer.parseInt(cantidadM);
            controlLogica.ModificarMaterial(material1, nombreM, proveedorM, cantidadMf, categoriaM);
            JOptionPane.showMessageDialog(null, "Se edito correctamente correctamente", "Información", JOptionPane.INFORMATION_MESSAGE);
            recargarPagina.cargarTabla();
            this.dispose();

        }

    }//GEN-LAST:event_BtnGuardarActionPerformed

