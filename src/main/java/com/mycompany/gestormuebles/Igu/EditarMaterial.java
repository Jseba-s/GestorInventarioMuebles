    public void MostrarMensaje(String mensaje, String tipoMensaje, String titulo) {
        int tipo = 0;
        if (tipoMensaje.equals("Info")) {
            tipo = JOptionPane.INFORMATION_MESSAGE;
        } else if (tipoMensaje.equals("Error")) {
            tipo = JOptionPane.WARNING_MESSAGE;

        }
        JOptionPane.showMessageDialog(null, mensaje, titulo, tipo);
    }

    private void cargarDatos(int id_Materiales) {
        this.material1 = controlLogica.traerMaterial(id_Materiales);

        TxtNombreMaterial.setText(material1.getNombreMaterial());
        TxtCantidadMaterial.setText(String.valueOf(material1.getCantidadMaterial()));
        //cargar categorias
        List<CategoriaMaterial> listaCategorias = controlLogica.traerCatogorias();
        if (listaCategorias != null) {
            for (CategoriaMaterial categorias : listaCategorias) {
                CbxCategoriaMaterial.addItem(categorias.getNombreCategoria());
            }
        }

        String categoria = material1.getCategoriaMaterial().getNombreCategoria();

        int cantidadItem = CbxCategoriaMaterial.getItemCount();

        for (int i = 0; i < cantidadItem; i++) {
            if (String.valueOf(CbxCategoriaMaterial.getItemAt(i)).equals(categoria)) {
                CbxCategoriaMaterial.setSelectedItem(categoria);
            }
        }

        //cargar Proveedores
        List<Proveedor> listaProveedores = controlLogica.traerProveedores();
        if (listaProveedores != null) {
            for (Proveedor proveedores : listaProveedores) {
                CbxProveedorMaterial.addItem(proveedores.getNombreProveedor());
            }
        }

        String proveedor = material1.getMaterialProveedor().getNombreProveedor();

        int cantidadItem2 = CbxProveedorMaterial.getItemCount();

        for (int j = 0; j < cantidadItem2; j++) {
            if (String.valueOf(CbxProveedorMaterial.getItemAt(j)).equals(proveedor)) {
                CbxProveedorMaterial.setSelectedItem(proveedor);
            }
        }
    }

