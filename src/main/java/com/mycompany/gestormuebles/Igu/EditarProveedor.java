    private void cargarDatos(int id_usuario) {
        this.proveedor1 = controlLogica.traerProveedores(id_usuario);

        TxtNombreProveedor.setText(proveedor1.getNombreProveedor());
        TxtcontactoProveedor.setText(proveedor1.getContactoProveedor());
        TxtTelefonoProveedor.setText(proveedor1.getTelefonoProveedor());
        TxtCorreoProveedor.setText(proveedor1.getCorreoProveedor());
        TxtDireccionProveedor.setText(proveedor1.getDireccion());

        if (proveedor1.getEstado().equals("ACTIVO")) {
            CmbEstado.setSelectedIndex(0);
        } else {
            CmbEstado.setSelectedItem(1);
        }
    }

    public void MostrarMensaje(String mensaje, String tipoMensaje, String titulo) {
        int tipo = 0;
        if (tipoMensaje.equals("Info")) {
            tipo = JOptionPane.INFORMATION_MESSAGE;
        } else if (tipoMensaje.equals("Error")) {
            tipo = JOptionPane.WARNING_MESSAGE;

        }
        JOptionPane.showMessageDialog(null, mensaje, titulo, tipo);
    }

