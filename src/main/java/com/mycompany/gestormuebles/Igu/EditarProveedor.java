    private void BtnGuardarProveedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarProveedorActionPerformed
        String nombreEp = TxtNombreProveedor.getText();
        String contactoEp = TxtcontactoProveedor.getText();
        String telefonoEp = TxtTelefonoProveedor.getText();
        String direccionEp = TxtDireccionProveedor.getText();
        String correoEp = TxtCorreoProveedor.getText();
        String estadoP = (String) CmbEstado.getSelectedItem();
        if (nombreEp.isEmpty() || contactoEp.isEmpty() || telefonoEp.isEmpty() || direccionEp.isEmpty() || correoEp.isEmpty() || estadoP.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por Favor los campos deben estar completados", "Información", JOptionPane.INFORMATION_MESSAGE);

        } else {
            controlLogica.modificarProveedor(proveedor1, nombreEp, contactoEp, telefonoEp, direccionEp, correoEp, estadoP);
            JOptionPane.showMessageDialog(null, "Se guardo correctamente", "Información", JOptionPane.INFORMATION_MESSAGE);
            recargarPagina.CargarTabla();
            this.dispose();

        }

    }//GEN-LAST:event_BtnGuardarProveedorActionPerformed

