    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        // TODO add your handling code here:

        if (TblRecetasCreadas.getRowCount() > 0) {
            if (TblRecetasCreadas.getSelectedRow() != -1) {
                int idReceta = Integer.parseInt(String.valueOf(TblRecetasCreadas.getValueAt(TblRecetasCreadas.getSelectedRow(), 0)));
                controlLogica.eliminarReceta(idReceta);
                MostrarMensaje("Se borro correctamente", "Info", "Eliminar");
                cargarTabla();
            } else {
                MostrarMensaje("No se selecciono a un elemento a borrar", "Error", "Error");

            }
        } else {
            MostrarMensaje("La tabla no tiene datos", "Error", "Error");

        }
        cargarTabla();

    }//GEN-LAST:event_BtnEliminarActionPerformed

