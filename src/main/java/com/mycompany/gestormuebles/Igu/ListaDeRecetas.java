    private void cargarTabla() {
        DefaultTableModel modelotabla = new DefaultTableModel() {

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        String titulos[] = {"Id", "Nombre", "Descripción", "Materiales"};
        modelotabla.setColumnIdentifiers(titulos);

        List<Receta> listaRecetas = controlLogica.traerRecetas();

        if (listaRecetas != null) {
            for (Receta recetas : listaRecetas) {
                StringBuilder materialesTexto = new StringBuilder("<html>");
                List<Receta_Material> listaMateriales = recetas.getListaMateriales();
                if (listaMateriales != null) {
                    for (Receta_Material rm : listaMateriales) {
                        String nombre = rm.getMaterialReceta().getNombreMaterial();
                        double cantidad = rm.getCantidad();
                        materialesTexto.append(nombre).append(" (").append(cantidad).append(")").append("<br>");

                    }
                }
                materialesTexto.append("</html>");
                if (materialesTexto.length() > 0) {
                    materialesTexto.setLength(materialesTexto.length() - 1);
                }

                Object[] fila = {
                    recetas.getIdReceta(),
                    recetas.getNombreReceta(),
                    recetas.getDescripcionReceta(),
                    materialesTexto.toString()
                };
                modelotabla.addRow(fila);

            }
            TblRecetasCreadas.setModel(modelotabla);
            TblRecetasCreadas.getColumnModel().getColumn(3).setPreferredWidth(200);
            TblRecetasCreadas.setRowHeight(60);
        }
        
  
    }
    
              
    public void MostrarMensaje(String mensaje, String tipoMensaje, String titulo) {
        int tipo = 0;
        if (tipoMensaje.equals("Info")) {
            tipo = JOptionPane.INFORMATION_MESSAGE;
        } else if (tipoMensaje.equals("Error")) {
            tipo = JOptionPane.WARNING_MESSAGE;

        }
        JOptionPane.showMessageDialog(null, mensaje, titulo, tipo);
    }
