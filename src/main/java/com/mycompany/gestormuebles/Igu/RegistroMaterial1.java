    private void CargarDatos() {
        cargarProveedor();
        cargarCategoria();
    }

    private void cargarProveedor() {
        List<Proveedor> listaproveedor = controlLogica.traerProveedores();
        if (listaproveedor != null) {
            for (Proveedor proveedor : listaproveedor) {
                CbxProveedorMaterial.addItem(proveedor.getNombreProveedor());
            }
        }
    }

    void cargarTabla() {
        //Definir Modelo de la tabla y que las columnas no sean editables
        DefaultTableModel tabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        //Creacion de los nombres de la columna
        String titulos[] = {"ID", "Nombre", "Categoria", "Proveedor", "Cantidad"};
        tabla.setColumnIdentifiers(titulos);

        //Cargar los datos
        List<Materiales> listaMateriales = controlLogica.traerMateriales();

        //Recorrer la lista
        if (listaMateriales != null) {
            for (Materiales materiales : listaMateriales) {
                Object[] objeto = {materiales.getIdMaterial(), materiales.getNombreMaterial(), materiales.getCategoriaMaterial().getNombreCategoria(), materiales.getMaterialProveedor().getNombreProveedor(), materiales.getCantidadMaterial()};
                tabla.addRow(objeto);
            }
        }
        TblDatosMateriales.setModel(tabla);
    }

    private void cargarCategoria() {
        List<CategoriaMaterial> listaCategoria = controlLogica.traerCatogorias();
        if (listaCategoria != null) {
            for (CategoriaMaterial categoriaMaterial : listaCategoria) {
                CbxCategoriaMaterial.addItem(categoriaMaterial.getNombreCategoria());
            }
        }
    }

    public void MostrarMensaje(String mensaje, String tipoMensaje, String titulo) {
        int tipo = 0;
        if (tipoMensaje.equals("Info")) {
            tipo = JOptionPane.INFORMATION_MESSAGE;
        } else if (tipoMensaje.equals("Error")) {
            tipo = JOptionPane.WARNING_MESSAGE;

        }
        JOptionPane.showMessageDialog(null, mensaje, titulo, tipo);
    }
