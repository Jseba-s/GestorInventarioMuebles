    private void BtnGuardarRecetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarRecetaActionPerformed
        // TODO add your handling code here:
        String nombreReceta = TxtNombreReceta.getText();
        String descripcionReceta = TxtAreaDescripcion.getText();

        if (nombreReceta.isEmpty() || descripcionReceta.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe completar todos los campos.");
            return;
        }

        Receta receta = new Receta();
        receta.setNombreReceta(nombreReceta);
        receta.setDescripcionReceta(descripcionReceta);
        controlLogica.guardarReceta(receta);

        DefaultTableModel modelo = (DefaultTableModel) TblMaterialagregado.getModel();

        for (int i = 0; i < modelo.getRowCount(); i++) {
            int idmaterial = Integer.parseInt(modelo.getValueAt(i, 0).toString());
            int cantidad = Integer.parseInt(modelo.getValueAt(i, 3).toString());

            Materiales mat = controlLogica.traerMaterial(idmaterial);

            if (mat != null) {
                Receta_Material recetaMaterial = new Receta_Material();
                recetaMaterial.setRecetaMaterial(receta);
                recetaMaterial.setMaterialReceta(mat);
                recetaMaterial.setCantidad(cantidad);
                controlLogica.guardarRecetaMaterial(recetaMaterial);
            }
        }
        JOptionPane.showMessageDialog(null, "Receta registrada exitosamente", "Información", JOptionPane.INFORMATION_MESSAGE);
        limpiarDatos();
    }//GEN-LAST:event_BtnGuardarRecetaActionPerformed

    private void BtnEliminarMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarMaterialActionPerformed
        // TODO add your handling code here:
        if (TblMaterialagregado.getRowCount() > 0) {
            if (TblMaterialagregado.getSelectedRow() != -1) {
                DefaultTableModel modelo = (DefaultTableModel) TblMaterialagregado.getModel();
                modelo.removeRow(TblMaterialagregado.getSelectedRow());
                JOptionPane.showMessageDialog(this, "Material eliminado de la receta.");
            } else {
                JOptionPane.showMessageDialog(this, "Seleccione un material para eliminar.");

            }
        } else {
            JOptionPane.showMessageDialog(this, "No hay materiales para eliminar.");

        }
    }//GEN-LAST:event_BtnEliminarMaterialActionPerformed

