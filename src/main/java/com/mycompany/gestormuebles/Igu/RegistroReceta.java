    void cargarTablaAgregados() {
        //Definir Modelo de la tabla y que las columnas no sean editables
        DefaultTableModel tabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return true;
            }
        };

        //Creacion de los nombres de la columna
        String titulos[] = {"ID", "Nombre", "Categoria", "Cantidad"};
        tabla.setColumnIdentifiers(titulos);

        TblMaterialagregado.setModel(tabla);
        //Cargar los datos
        //List<Materiales> listaMateriales = controlLogica.traerMateriales();
    }

    public void agregarMaterial(Materiales material, int cantidad) {
        DefaultTableModel modelo = (DefaultTableModel) TblMaterialagregado.getModel();
        modelo.addRow(new Object[]{
            material.getIdMaterial(), material.getNombreMaterial(), material.getCategoriaMaterial().getNombreCategoria(), cantidad
        });

    }

    private void cargarTablaMaterial() {
        DefaultTableModel tablaMateriales = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        String TItulosMateriales[] = {"ID", "Nombre", "Categoria", "Cantidad"};
        tablaMateriales.setColumnIdentifiers(TItulosMateriales);

        listaMateriales = controlLogica.traerMateriales();

        if (listaMateriales != null) {
            for (Materiales materiales : listaMateriales) {
                Object[] objecto = {materiales.getIdMaterial(), materiales.getNombreMaterial(), materiales.getCategoriaMaterial().getNombreCategoria(), materiales.getCantidadMaterial()};
                tablaMateriales.addRow(objecto);
            }
        }
        TblMateriales.setModel(tablaMateriales);

    }

    private void limpiarDatos() {
        TxtNombreReceta.setText("");
        TxtAreaDescripcion.setText("");
        DefaultTableModel modelo = (DefaultTableModel) TblMaterialagregado.getModel();
        modelo.setRowCount(0);
    }
